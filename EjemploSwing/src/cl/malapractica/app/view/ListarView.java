    public void limpiarTabla(){
        int rowCount = tableModel.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            tableModel.removeRow(i);
        }
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        limpiarTabla();
        
        IEmpleadoController empleadoController = new EmpleadoControllerImpl();
       for(Empleado empleado: empleadoController.selectAll()){
           tableModel.addRow(new Object[]{empleado.getRut(), empleado.getComuna(), empleado.getCapacitacion(), empleado.getTipoContrato()});
           
       }
    }//GEN-LAST:event_jButton2ActionPerformed

